package com.walking.lesson1_varaibles;

/**
 * Используя несколько переменных типа char вывести свое имя в консоль.
 */
public class Task2MultipleChar {
    public static void main(String[] args) {
        char g = 'G', a = 'a', l = 'l', i = 'i', n = 'n';
        System.out.print(g);
        System.out.print(a);
        System.out.print(l);
        System.out.print(i);
        System.out.print(n);
        System.out.print(a);

    }
}
