package com.walking.lesson1_varaibles;

/**
 * Используя несколько переменных типа char вывести свое имя в консоль.
 */
public class Task2MultipleChar {
    public static void main(String[] args) {
        char E = 'Е';
        char v = 'в';
        char g = 'г';
        char e = 'е';
        char n = 'н';
        char i = 'и';
        char iy = 'й';

        System.out.print(E);
        System.out.print(v);
        System.out.print(g);
        System.out.print(e);
        System.out.print(n);
        System.out.print(i);
        System.out.print(iy);
    }
}
