package com.walking.lesson1_varaibles;

/**
 * Используя одну переменную типа char вывести свое имя в консоль.
 */
public class Task2SingleChar {
    public static void main(String[] args) {
        char c = 'G';
        System.out.print(c);
        c = 'a';
        System.out.print(c);
        c = 'l';
        System.out.print(c);
        c = 'i';
        System.out.print(c);
        c = 'n';
        System.out.print(c);
        c = 'a';
        System.out.print(c);
    }
}
