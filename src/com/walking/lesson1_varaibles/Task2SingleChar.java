package com.walking.lesson1_varaibles;

/**
 * Используя одну переменную типа char вывести свое имя в консоль.
 */
public class Task2SingleChar {
    public static void main(String[] args) {
        char ch = 'М';
        System.out.print(ch);
        ch = 'а';
        System.out.print(ch);
        ch = 'к';
        System.out.print(ch);
        ch = 'с';
        System.out.print(ch);
        ch = 'и';
        System.out.print(ch);
        ch = 'м';
        System.out.print(ch);
    }
}
