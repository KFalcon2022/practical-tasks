package com.walking.lesson1_varaibles;

/**
 * Используя одну переменную типа char вывести свое имя в консоль.
 */
public class Task2SingleChar {
    public static void main(String[] args) {
        char ch = 'M';
        System.out.print(ch);
        ch = 'a';
        System.out.print(ch);
        ch = 'r';
        System.out.print(ch);
        ch = 'i';
        System.out.print(ch);
        ch = 'a';
        System.out.print(ch);
    }
}
