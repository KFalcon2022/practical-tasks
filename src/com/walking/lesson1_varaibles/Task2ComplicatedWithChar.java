package com.walking.lesson1_varaibles;

/**
 * Усложнение для внимательных: вывести ваше имя посимвольно,
 * где каждая буква — на новой строке, не используя System.out.println().
 * Реализуется с использованием char.w
 */
public class Task2ComplicatedWithChar {
    public static void main(String[] args) {
        char ch = 'М';
        System.out.print(ch);
        System.out.print('\n');
        ch = 'а';
        System.out.print(ch);
        System.out.print('\n');
        ch = 'к';
        System.out.print(ch);
        System.out.print('\n');
        ch = 'с';
        System.out.print(ch);
        System.out.print('\n');
        ch = 'и';
        System.out.print(ch);
        System.out.print('\n');
        ch = 'м';
        System.out.print(ch);
    }
}
